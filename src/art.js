export function cattransform(originalgrid,facegrid) {
	
	let grid = JSON.parse(JSON.stringify(originalgrid))
	
	let x = 0
	let y = 0
	let i,j
	for (i = 0; i < grid.length; i++) {
		for (j = 0; j < grid[0].length; j++) {
			if (grid[i][j] === 9) {
				x = i
				y = j
				break
			}
		}
	}
	
	for (i = 0; i < facegrid.length; i++) {
		for (j = 0; j < facegrid[0].length; j++) {
			grid[x+i][y+j] = facegrid[i][j]
		}
	}
	
	
	function colorize(x) {
		const d = {
			0: "#282c34",
			1: "black",
			2: "#D0CBE1",
			3: "#858195",
			4: "orange"
		}
		return d[x]
	}
	
	return grid.map(row => row.map(px => colorize(px)))
	
}

